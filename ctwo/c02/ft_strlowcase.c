#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] = str[i] + 32;
			i++;
		}
		else
			i++;
	}
	return (str);
}

int	main(void)
{
	char	str[] = "BONJOUR";

	printf("%s\n", ft_strlowcase(str));
	return (0);
}
